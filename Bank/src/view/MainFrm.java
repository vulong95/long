    void loadTable(List<Bank> list) {
        Vector colum = new Vector();
        colum.add("code");
        colum.add("name");
        colum.add("phone");
        colum.add("address");
        colum.add("price");
        Vector rows = new Vector();
        for (Bank b : list) {
            Vector row = new Vector();
            row.add(b.getCode());
            row.add(b.getName());
            row.add(b.getPhone());
            row.add(b.getAddress());
            row.add(b.getPrice());
            rows.add(row);
        }
        tblBank.setModel(new DefaultTableModel(rows, colum));
        tblBank.updateUI();
        spnBank.setViewportView(this.tblBank);
    }

