    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String code = txtCode.getText().trim();
        String name = txtName.getText().trim();
        int phone = Integer.parseInt(txtPhone.getText().trim());
        String address = txtAddress.getText().trim();
        double price = Double.parseDouble(txtPrice.getText().trim());
        Bank b = new Bank(code, name, phone, address, price);
        try {
            if (client.check(code)) {
                if (client.add(b) != null) {
                    JOptionPane.showMessageDialog(this, "Add Success");
                    loadTable(client.readAll());
                } else {
                    JOptionPane.showMessageDialog(this, "Code is already registered");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Fail, Code start with 0161 or 0162 and must have 11 digit");
            }
        } catch (RemoteException ex) {
            Logger.getLogger(MainFrm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String code = txtCode.getText().trim();
        double price = Double.parseDouble(txtPrice.getText().trim());
        Bank b = new Bank(code, price);
        try {
            if (client.update(b) != null) {
                JOptionPane.showMessageDialog(this, "Update Success");
                loadTable(client.readAll());
            } else {
                JOptionPane.showMessageDialog(this, "Update Fail");
            }
        } catch (RemoteException ex) {
            Logger.getLogger(MainFrm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

